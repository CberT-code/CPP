#include "header.h"

void			rush(int x, int y)
{
	int x2;
	int y2;

	y2 = 1;
	if (y < 1 || x < 1)
		return ;
	while (y2 <= y)
	{
		x2 = 1;
		while (x2 <= x)
		{
			if ((x2 == 1 || x2 == x) && (y2 == 1 || y2 == y))
				write(1, "o", 1);
			else if (x2 == 1 || x2 == x)
				write(1, "|", 1);
			else if (y2 == 1 || y2 == y)
				write(1, "-", 1);
			else
				write(1, " ", 1);
			x2++;
		}
		write (1, "\n", 1);
		y2++;
	}
}