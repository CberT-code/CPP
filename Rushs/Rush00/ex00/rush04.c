#include "header.h"

void			rush(int x, int y)
{
	int x2;
	int y2;

	y2 = 1;
	if (y < 1 || x < 1)
		return ;
	while (y2 <= y)
	{
		x2 = 1;
		while (x2 <= x)
		{
			if ((y2 == 1 && x2 == 1) || (y2 == y && x2 == x && x > 1 && y > 1))
				write(1, "A", 1);
			else if ((x2 == 1 && y2 == y) || (x2 == x && y2 == 1))
				write(1, "C", 1);
			else if (x2 == 1 || x2 == x || y2 == 1 || y2 == y)
				write(1, "B", 1);
			else
				write(1, " ", 1);
			x2++;
		}
		write (1, "\n", 1);
		y2++;
	}
}